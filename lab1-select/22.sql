-- 22.
-- Kiedy odbył się egzamin z poszczególnych przedmiotów w poszczególnych ośrodkach?
-- Dla każdego ośrodka wskazać przedmioty, z których przeprowadzano egzamin i datę tego
-- egzaminu. Dodatkowo uporządkować rezultat według daty zdawania egzaminu. Do
-- identyfikacji ośrodka i przedmiotu należy posłużyć się ich identyfikatorami.

